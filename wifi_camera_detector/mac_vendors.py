# 风险等级
def get_risk_level(port, vendor, rtsp_accessible):
    """根据检测结果判断风险等级"""
    # 高风险：有RTSP流且是摄像头厂商
    if rtsp_accessible and vendor and "摄像头" in vendor or "Camera" in vendor:
        return "高危", "red"

    # 中高风险：有RTSP流或开放摄像头端口
    if rtsp_accessible or port in [554, 555, 8000, 8008, 8200, 37777]:
        return "高风险", "orange"

    # 中风险：开放HTTP端口且厂商可疑
    if port in [80, 81, 82, 83, 84, 85, 88, 8080, 8081] and vendor:
        return "中风险", "yellow"

    # 低风险：其他情况
    return "低危", "green"
